     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $journals = Journal::all();
        $csvExporter = new \Laracsv\Export();
        $csv = $csvExporter->build($journals, ['id', 'date', 'debit', 'debit_sub_account', 'credit', 'credit_sub_account', 'amount', 'remark', 'user_id', 'created_at', 'updated_at'])->download();
        return response($csv);
    }

